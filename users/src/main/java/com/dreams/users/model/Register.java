package com.dreams.users.model;

import lombok.Getter;
import lombok.Setter;

public class Register {

	@Getter @Setter public String email;
	@Getter @Setter public String firstname;
	@Getter @Setter public String lastname;
	@Getter @Setter public String phone;
	@Getter @Setter public String password;
	@Getter @Setter public int type;
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getFirstname() {
		return firstname;
	}
	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}
	public String getLastname() {
		return lastname;
	}
	public void setLastname(String lastname) {
		this.lastname = lastname;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	
	
	
}
